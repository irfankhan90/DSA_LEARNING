public class Variable {
    public static void main(String[] args) {
        int x = 10;
        int y = 20;

        int sum = x + y;
        System.out.println(sum);
        System.out.println("sum of " + x + " and " + y + " is " + sum);

        int prd = x*y;
        System.out.println("product of " + x + " and " + y + " is " + prd);
    }
}
